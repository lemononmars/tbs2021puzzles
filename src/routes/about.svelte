	<li>เมื่อเล่นเสร็จแล้ว จะสามารถใส่ชื่อเพื่อจัดอันดับ ์โดยให้ใช้ชื่อเดียวกันกับชื่อใน gather.town</li>
	<li>ผู้เล่นทุกคนที่แก้ปริศนาได้ภายใน 26 พฤศจิกายน 23:59 น. จะได้รับของที่ระลึกจัดส่งให้หลังงาน</li>
	<li>ทีมงานขอสงวนสิทธิ์ในการเปลี่ยนของรางวัล</li>
