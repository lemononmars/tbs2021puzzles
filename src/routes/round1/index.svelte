<h1>วิธีตอบ</h1>
<ol>
	<li>คำตอบเป็นภาษาอังกฤษ (A-Z) 5 ตัวอักษรทุกข้อ โดยจะพิมพ์ตอบด้วยตัวใหญ่หรือเล็กก็ได้</li>
